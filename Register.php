    function validateForm() {
        var name = document.forms["Customer"]["Fnamee"].value;
        var userId = document.forms["Customer"]["idd"].value;
        var address = document.forms["Customer"]["addresss"].value;
        var email = document.forms["Customer"]["emaill"].value;
        var password = document.forms["Customer"]["passwordd"].value;
        var rePassword = document.forms["Customer"]["ree"].value;

        // Client-side validation
        if (name === "" || userId === "" || address === "" || email === "" || password === "" || rePassword === "") {
            alert("All fields must be filled out");
            return false;
        }

        if (password.length > 10 || !/[a-z]/.test(password) || !/[A-Z]/.test(password)) {
            alert("Password must be less than 10 characters and include both lowercase and uppercase letters");
            return false;
        }

        if (password !== rePassword) {
            alert("Passwords do not match");
            return false;
        }

        return true;
    }
